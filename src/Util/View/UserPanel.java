package Util.View;

public class UserPanel extends MyJPanel{
    private int userID;
    private int deviceID;
    public UserPanel() {
        this.userID = userID;
        this.deviceID = deviceID;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public int getDeviceID() {
        return deviceID;
    }

    public void setDeviceID(int deviceID) {
        this.deviceID = deviceID;
    }
}
